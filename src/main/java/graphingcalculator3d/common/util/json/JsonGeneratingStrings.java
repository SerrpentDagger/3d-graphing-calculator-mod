	public static String generateJson(String jsonType, String texture1, String texture2, String texture3)
	{
		String tempString = "";
		if (jsonType == "item_crystal")
		{
			tempString = itemCrystalJson;
			return tempString.replaceAll("item_texture_crystal", texture1);
		}
		else if (jsonType == "item_simple")
		{
			tempString = itemSimpleJson;
			return tempString.replaceAll("item_texture", texture1);
		}
		else if (jsonType == "item_jar_small")
		{
			tempString = itemJarSmallJson;
			tempString = tempString.replaceAll("item_texture_contents", texture1);
			tempString = tempString.replaceAll("item_texture_glass", texture2);
			return tempString.replaceAll("item_texture_lid", texture3);
		}
		else if (jsonType == "blockstate_simple")
		{
			tempString = blockstateSimpleJson;
			return tempString.replaceAll("block_texture", texture1);
		}
		else if (jsonType == "block_model_simple")
		{
			tempString = blockModelSimpleJson;
			return tempString.replaceAll("block_texture", texture1);
		}
		else if (jsonType == "item_block_simple")
		{
			tempString = itemBlockSimpleJson;
			return tempString.replaceAll("block_model", texture1);
		}
		else if (jsonType == "block_model_bottom_top")
		{
			tempString = blockModelBottomTop;
			tempString = tempString.replaceAll("texture_bottom", texture1);
			tempString = tempString.replaceAll("texture_top", texture2);
			return tempString.replaceAll("texture_side", texture3);
		}
		else if (jsonType == "block_model_calc")
		{
			return generateJson("block_model_bottom_top", "block_gc_cartesian_bottom", texture1 + "_top", "block_gc_cartesian_side");
		}
		else
		{
			System.out.println("generateJson failed. Invalid jsonType. Generating empty file for textures: " + texture1 + ", " + texture2 + ", " + texture3);
			return "";	
		}
	}
